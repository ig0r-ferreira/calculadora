def somar(a, b):
    return a + b


def subtrair(a, b):
    return a - b


def multiplicar(a, b):
    return a * b


def dividir(a, b):
    return a / b


v1 = float(input('Valor 1: ').strip())
v2 = float(input('Valor 2: ').strip())

print(somar(v1, v2))
print(subtrair(v1, v2))
print(multiplicar(v1, v2))
print(dividir(v1, v2))
